    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
      //Aqui fica o botao de salvar
      //Devemos chamar a classe Clientes para passar os dados
      Clientes dados = new Clientes();
      dados.setNome(txtNome.getText());
      dados.setCpf(txtCpf.getText());
      dados.setCelular(txtCelular.getText());
      dados.setEmail(txtEmail.getText());
      
      //Agora vamos chamar o metodo cadastro para cadastrar 
      Clientes.enviarDados(dados);
      limparCampos();
    }//GEN-LAST:event_botaoSalvarActionPerformed

